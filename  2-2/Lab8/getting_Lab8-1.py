from pythainlp import thai_consonants, thai_vowels, thai_tonemarks

# ---------------------------------------------------------
# 1. สร้างฐานข้อมูล (Mapping) ตามภาพที่ 1
# เรียงลำดับ string ตามคะแนน 1 ถึง 9
# หมายเหตุ: ฉันแปลงรูป "อำ" เป็น "ำ" และใส่ "ณ" ไว้ในกลุ่ม 5 ตามหลักทั่วไป (เนื่องจากในภาพกลุ่ม 5 ตัวอักษรดูคล้ายกัน)
score_table = [
    "กดถทภฤาำุ่",      # กลุ่มคะแนน 1
    "ขชงบปเแู้",       # กลุ่มคะแนน 2
    "ฆฑฒต๊",           # กลุ่มคะแนน 3
    "คธญรษะโัิ",       # กลุ่มคะแนน 4
    "ฉณฌนมหฮฎฬึ",      # กลุ่มคะแนน 5
    "จลวอใ",           # กลุ่มคะแนน 6
    "ซศสีื๋",          # กลุ่มคะแนน 7
    "ผฝพฟย็",          # กลุ่มคะแนน 8
    "ฏฐไ์"             # กลุ่มคะแนน 9
]

# ใช้เทคนิค Dictionary Comprehension สร้างคู่มือเทียบเลขในบรรทัดเดียว
numerology_map = {char: score+1 for score, chars in enumerate(score_table) for char in chars}

# ---------------------------------------------------------
# 2. ส่วนคำนวณและแสดงผล (Calculation)
name = "ยูกิ"  # <-- เปลี่ยนชื่อที่ต้องการทำนายตรงนี้

# คำนวณผลรวม
total_score = sum(numerology_map.get(char, 0) for char in name)

print(f"ชื่อ: {name}")
print(f"ผลรวมเลขศาสตร์: {total_score}")

# ---------------------------------------------------------
# 3. แปลผลตามเกณฑ์ภาพที่ 2 (Interpretation)
# ---------------------------------------------------------
# ใช้ Dictionary เก็บเกณฑ์ตัวเลข เพื่อให้แก้ไของ่ายและ Code สั้น
predictions = {
    "ดีมาก (Very Good)": [2, 4, 5, 6, 9, 14, 15, 19, 23, 24, 36, 41, 42, 45, 46, 50, 51, 53, 55, 56, 59, 63, 64, 65],
    "ดี (Good)":         [20, 32, 40, 44, 69, 79],
    "ให้โทษ (Harmful)":  [27, 29, 30]
}

# หาคำทำนาย (Logic: วนลูปเช็คว่าผลรวมตรงกับกลุ่มไหน)
result_meaning = "เกณฑ์ปกติ/ไม่ระบุ (Neutral)" # ค่าเริ่มต้นกรณีไม่ตรงกับเลขใดเลย

for meaning, numbers in predictions.items():
    if total_score in numbers:
        result_meaning = meaning
        break

print(f"ผลทำนาย: {result_meaning}")